package com.traines.corejava;

public class ForloopEx {

	public static void main(String[] args) {
		for(int i = 1; i <=10; i++) {//2 3 
			
			System.out.println(i);//1 2 3 4 5 ...10
			
		}
		System.out.println("***********************************");
		
		for (int i = 2; i <= 10; i = i + 2) { //4 6 
			
			
			System.out.println(i);//2 4 6 ....10
		}

	}

}
