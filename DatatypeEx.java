package corejava;

public class DatatypeEx {

	public static void main(String[] args) {

		byte b = 15; // -128 to 127

		short s = 20000; // -32768to 32767

		int i = 2144566;

		long contact = 9845623L;

		char c = 'f';

		boolean bool = true;

		float f = 2.523333f;// end with f

		double d = 30.5;

		System.out.println(b + "  " + s + " " + i + "  " + c + " " + bool + " " + f + "  " + d + " " + contact);

	}

}
