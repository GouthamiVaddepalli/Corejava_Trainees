package com.traines.corejava;

public class UnaryOperatorEx {

	public static void main(String[] args) { // unary operator
		int a = 20;
		int b = 10;
		System.out.println(a++);//20 + 1 = 21
		
		System.out.println(++a);//21+1  =22
		
		System.out.println(b--); //10-1=9 
		System.out.println(b); // 9
		
		System.out.println(--b);//9-1 = 8

	}

}
